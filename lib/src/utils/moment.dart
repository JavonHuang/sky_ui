class SkyMoment {
  DateTime now() {
    return DateTime.now();
  }

  int get year {
    return DateTime.now().year;
  }

  int get month {
    return DateTime.now().month;
  }

  int get day {
    return DateTime.now().day;
  }

  int get hour {
    return DateTime.now().hour;
  }

  int get minute {
    return DateTime.now().minute;
  }

  int get second {
    return DateTime.now().second;
  }
}
